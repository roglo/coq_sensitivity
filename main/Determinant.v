Theorem cart_prod_rep_seq_inj : ∀ n i j,
  n ≠ 0
  → i < n ^ n
  → j < n ^ n
  → nth i (cart_prod_rep_seq n) [] = nth j (cart_prod_rep_seq n) []
  → i = j.
Proof.
