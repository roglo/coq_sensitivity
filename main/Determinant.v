  now rewrite mat_transp_nrows, squ_mat_ncols.
}
rewrite (det_any_permut_l Hic Hop H10 M Hnz Hr Hsm Hs).
rewrite (det_any_permut_r Hic Hop H10 (M⁺)%M Hnz Hr' Hts Hs).
