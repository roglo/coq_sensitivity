  (op_comm : ∀ a b, op a b = op b a)
  (op_assoc : ∀ a b c, op a (op b c) = op (op a b) c),
  Permutation l1 l2
  → iter_list l1 (λ (c : T) (i : nat), op c (f i)) d =
    iter_list l2 (λ (c : T) (i : nat), op c (f i)) d.
Proof.
