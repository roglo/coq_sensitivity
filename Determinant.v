Qed.

Theorem vect_el_nat_of_canon_permut_ub : ∀ n (v : vector (S n) nat) i,
  (∀ i, i < S n → vect_el v i < S n)
  → (∀ i j, i < S n → j < S n → i ≠ j → vect_el v i ≠ vect_el v j)
  → i < n
  → vect_el (nat_of_canon_permut_sub_vect v n) i < n.
Proof.
intros * Hvn Hn Hin.
destruct n; [ easy | ].
cbn - [ "<?" ].
